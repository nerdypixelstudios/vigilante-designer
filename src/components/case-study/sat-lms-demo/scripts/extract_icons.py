"""
extract_icons.py
----------------
Reads the LMS Icons.jsx file and extracts only the named exports
required by the SAT LMS Interactive Demo package.

Usage (run from inside sat-lms-interactive-demo/):
  python scripts/extract_icons.py

Output:
  components/icons/DemoIcons.jsx
"""

